    <h1 class="t1_cc">
        TECNICO LABORAL EN RECREACION Y DEPORTE
    </h1>
    <p>
        Un Técnico Laboral en Recreación y Deporte es un profesional capacitado para planificar, organizar y dirigir actividades recreativas, deportivas y culturales. Su objetivo principal es promover la salud, el bienestar y la integración social a través del juego, el ejercicio físico y la interacción con otros
    </p>
    
    <h2 class="t2_cc">¿Qué aprenderás en la media técnica de Deportes?</h2>
    <p class="s1_cc">
        Planificación y organización de actividades
    </p>
    <p>
        Aprenderás a diseñar programas y eventos recreativos, deportivos y culturales adaptados a diferentes públicos (niños, adultos mayores, personas con discapacidad, etc.).
    </p>
    <p class="s2_cc">
        Gestión de espacios y recursos
    </p>
    <p>
        Sabrás cómo utilizar de manera eficiente los diferentes espacios y materiales para desarrollar las actividades.
    </p>
    <p class="s3_cc">
        Liderazgo y animación
    </p>
    <p>
        Desarrollarás habilidades para dirigir grupos, motivar a las personas y crear un ambiente divertido y seguro.
    </p>
    <p class="s4_cc"> Primeros auxilios </p>
    <p>  Aprenderás a brindar atención básica en caso de emergencia.
    </p>
    <p class="s5_cc">Normas de seguridad</p>
    <p> Conocerás las normas y protocolos de seguridad para garantizar el bienestar de los participantes.
    </p>
    <h3 class="t3_cc">¿Para qué sirve la media técnica de Deportes?
    </h3>
    <p class="s6_cc"> Promover la salud y el bienestar</p>
    <p>Ayudar a las personas a llevar un estilo de vida más activo y saludable.</p>
    <p class="s7_cc">Fomentar la integración social</p>
    <p> Crear espacios donde las personas puedan interactuar y relacionarse.</p>
    <p class="s8_cc"> Desarrollar habilidades sociales</p>
    <p> Mejorar la comunicación, el trabajo en equipo y la resolución de conflictos.</p>
    <p class="s9_cc">Amplias oportunidades laborales</p>
    <p>Podrás trabajar en centros deportivos, recreativos, educativos, empresas, organizaciones sociales y más.</p>
    <h4 class="t4_cc">Perfil obtenido al culminar la media técnica</h4>
    <ul class="ul_cc">
        <li>Formación en educación física o ciencias del deporte</li>
        <li>Conocimiento de diversas disciplinas deportivas y técnicas de entrenamiento</li>
        <li>Habilidades para planificar y ejecutar programas deportivos escolares</li>
        <li>Capacidad para promover hábitos saludables y trabajo en equipo</li>
    </ul>
