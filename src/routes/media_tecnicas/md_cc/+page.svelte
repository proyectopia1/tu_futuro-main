<h1 class="t1_cc">
    CONTABILIZACION DE OPERACIONES COMERCIALES Y FINANCIERAS
</h1>
<p >
    La media técnica en contabilización de operaciones comerciales y financieras
     es un programa educativo que te prepara para gestionar y registrar
     las transacciones económicas de una empresa.
    En otras palabras, aprenderás a llevar los libros contables de una manera correcta y eficiente
</p>

<h2 class="t2_cc">¿Qué aprenderás en la media técnica de comercio?</h2>
<p class="s1_cc">
    Conceptos básicos de contabilidad
</p>
<p>
    Entenderás cómo funcionan los estados financieros, el balance general, el estado de resultados y el flujo de efectivo.
</p>
<p class="s2_cc">
    Normas Contables
</p>
<p>
    Aprenderás las normas y estándares contables nacionales e internacionales para garantizar que la información financiera sea confiable y comparable.
</p>
<p class="s3_cc">
    Registros contables
</p>
<p>
    Sabrás cómo registrar las transacciones en los libros contables, utilizando software especializado.
</p>
<p class="s4_cc"> Impuestos </p>
<p> Conocerás las obligaciones tributarias de las empresas y cómo calcular y pagar los impuestos correspondientes.
</p>
<p class="s5_cc">Análisis financiero</p>
<p>Aprenderás a interpretar los estados financieros para tomar decisiones de negocio basadas en datos.
</p>
<h3 class="t3_cc">¿Para qué sirve la media técnica de comercio?
</h3>
<p class="s6_cc">Gestión financiera</p>
<p>Podrás controlar los ingresos y gastos de una empresa, identificar áreas de mejora y tomar decisiones estratégicas.</p>
<p class="s7_cc">Cumplimiento legal</p>
<p>Te asegurarás de que la empresa cumpla con todas las obligaciones contables y fiscales.</p>
<p class="s8_cc">Soporte a la toma de decisiones</p>
<p>Proporcionarás información financiera confiable a los directivos para que puedan tomar decisiones informadas.</p>
<p class="s9_cc">Emprendimiento</p>
<p>Podrás iniciar tu propio negocio y llevar una contabilidad adecuada.</p>
<h4 class="t4_cc">Perfil obtenido al culminar la media técnica</h4>
<ul class="ul_cc">
    <li>Conocimientos en contabilidad básica y avanzada</li>
    <li>Manejo de software contable y hojas de cálculo</li>
    <li>Comprensión de normativas fiscales y tributarias colombianas</li>
    <li>Habilidades para gestionar presupuestos y realizar informes financieros</li>
    <li>Contabilización de operaciones comerciales y financieras es una excelente opción para ti si te apasiona la contabilidad y quieres desarrollar una carrera en el mundo de la gestión comercial
    </li>
</ul>
